N = int(input())
M = 3 * N

welcome_index = ((N - 1)/2) + 1
welcome_dash = int((M - 7)/2)
stick_num1 = 0
counter = 0
for num in range(1, N + 1):

    if num < welcome_index:
        #print("-", end="")
        print(".|."*(num+stick_num1))
        #print("-")
        stick_num1 += 1

    elif num == welcome_index:
        print(f'{"-"*welcome_dash}{"WELCOME"}{"-"*welcome_dash}')
    
    elif num > welcome_index:
        #print("-", end="")
        print(".|."*(num-counter))
        #print("-")
        counter += 3
